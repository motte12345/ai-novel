- 第1章で人物が1人しか出ていない場合、第2〜3章で**もう1人加わると物語が動きやすい**
- 場所は前章のものを引き継ぐのが基本だが、**場面転換（時間経過、別の部屋、外への移動）は積極的に使う**
- 同じ場所・同じ視点に4章以上留まらない

